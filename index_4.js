// while 04 ejercicio//

let i = 8;
let fin = 2;
while (i >= fin) {
  let x = (i ** 2) / 2;
  console.log(`${x}`);
  i--;
}

// do...while //

let i = 8;
let fin = 2;
do {
  let x = (i ** 2) / 2;
  console.log(`${x}`);
  i--;
} while (i >= fin);

// for //

let fin = 2;
for (let i = 8; i >= fin; i--) {
  let x = (i ** 2) / 2;
  console.log(`${x}`);
}
