// while 03 ejercicio//

let i = 0;
let fin = 5;
while (i <= fin) {
  let x = i ** 2;
  console.log(`${x}`);
  i++;
}

// do...while //

let i = 0;
let fin = 5;
do {
  let x = i ** 2;
  console.log(`${x}`);
  i++;
} while (i <= fin);

// for //

let fin = 5;
for (let i = 0; i <= fin; i++) {
  let x = i ** 2;
  console.log(`${x}`);
}